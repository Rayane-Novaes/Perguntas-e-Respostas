package com.senac.perguntasrespostas;

public class Ranking {
	private String nome;
    private int pontos;
    
    public Ranking() {}
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public int getPontos() {
        return pontos;
    }
    public void setPontos(int pontos) {
	this.pontos = pontos;
    }
}
